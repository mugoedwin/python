#for loop-used to iterate through a sequence
#loop through a list
fruits=["apple","banana","mango","pineapple"]
for fruit in fruits:
     print(fruit)
#loop through a string
for x in "hello":
     print(x)
#loop through a range

for z in range (5):
    print(z)
for a in range (13,17):
    print(a)
#break-terminates a loop
hobbies=["reading","coding","swimming","running"]
for hobby in hobbies:
    print(hobby)
    if hobby=="swimming":
        break





