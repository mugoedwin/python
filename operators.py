#operators
#arithmetic operators-+,-,*
x=10
y=20
#ADDITION
print(f"the sum of{x} and {y} is {x+y} ")
#SUBTRACTION
sub=y-x
print("The difference btwn",y,"and",x,"is",sub)
#MULTIPLICATION
print(f"The product of{x} and {y} is {x*y}")
#DIVISION
print(f"The division of{y} and {x} is {y/x}")
#modulus-%-remainder of division
z=15
print(f"The remainder of division of{z} and {x} is {z%x}")
#power **
print(f"{x} power 2 is {x**2}")
#comparison operators >,>,>=,<=,==,/=
#greater than >
print(f"{x} greater than {y} {x>y}")
#less than <
print(f"{x} less than {y} {x<y}")
#equal to ==
print(f"{x} equals {y} {x==y}")
#greater than or equal to
print(f"{x} greater than or equal{y}? {x>=y}")
#less than or equal to
print(f"{x} less than or equal{y}? {x<=y}")
#not equal to !=
print(f"{x} not equal to {y}? {x!=y}")
#logical operators  and or and not
#AND-returns true if both expersions are true
z=3
print(z<10 and z>1 )
print(z>5 and z<1 )
#or-returns true if one expression is true
print(z>5 or z>1)
#not-returns the reverse
print( not(z>5))
#ASSIGNMENT OPERATORS =
a=20
print(a)
#+=
a+=5 #a=a+5
print(a)
#-=
a-=3
print(a)














